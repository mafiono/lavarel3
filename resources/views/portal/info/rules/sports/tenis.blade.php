<h2>Geral</h2>
<p><b>Apostas serão válidas em caso de qualquer uma das seguintes circunstâncias</b>:</p>
<ul>
    <li>Alteração de horário e/ou dia da partida</li>
    <li>Alteração do local da partida</li>
    <li>Alteração de court coberto para court ao ar livre ou vice-versa</li>
    <li>Alteração de superfície (antes ou durante uma partida)</li>
    <li>Atraso no início da partida não afetará a validade das apostas, nem uma suspensão, desde que o jogo seja retomado e a partida concluída.</li>
</ul>
<p><b>Vencedor do encontro</b>: Aposta quem será o vencedor do encontro. Todas as apostas são consideradas nulas se o número regulamentar de sets não for realizado</p>
<p><b>Aposta nos sets</b>: Aposta como terminará o jogo relativamente aos sets.</p>
<p><b>Games Handicap</b>: Um tipo de Aposta onde é dada uma vantagem inicial a um dos participantes (Handicap), portanto o resultado da Aposta depende do Handicap ser coberto ou não no final.</p>
<p><b>Total de jogos</b>: Under/Over - Aposta no número final de jogos, acima ou abaixo de certo valor.</p>
<p><b>Total de Sets</b>: Aposta no número total de sets, acima ou abaixo de certo valor.</p>
<p><b>Primeiro vencedor do set</b>: Aposta no vencedor do primeiro set.</p>
<p><b>Resultado exato do 1º Set</b>: Aposta no resultado correto dos jogos do primeiro set.</p>
<p><b>Aposta no total de jogos do 1º set</b>: Aposta no número total de jogos do 1º set, acima ou abaixo de certo valor.</p>
<p><b>Existirá Tie-Break no encontro?</b>: Aposta se haverá Tie-Break ou não na partida.</p>
<p><b>Existirá Tie-Break no 1º Set?</b>: Aposta se haverá Tie-Break ou não no 1º Set.</p>
<p><b>Jogador 1 - Total de jogos ganhos</b>: Aposta no número total de jogos ganhos pelo jogador 1, acima ou abaixo de certo valor.</p>
<p><b>Jogador 2 - Total de jogos ganhos</b>: Aposta no número total de jogos ganhos pelo jogador 2, acima ou abaixo de certo valor.</p>
<p><b>1º Set e Jogo</b>: Aposta em conjunto do vencedor do primeiro set e o vencedor do jogo final.</p>
<p><b>Primeiro Break no 1º Set</b>: Aposta se o jogador 1 ou jogador 2 conseguirá ou não realizar um break no primeiro set.</p>
<p><b>Será o jogo ganho através de um às?</b>: Aposta se o jogo será ou não finalizado com uma às.</p>
<p><b>Haverá na partida a Vitória do jogador 1 e no segundo set do jogador 2 ou W2/W1</b>: Aposta se os 2 primeiros sets serão repartidos com uma vitória para cada jogador.</p>
<p><b>Jogador 1: Break no jogo 1 ou no jogo 2: Aposta se o jogador 1 sofre o break no jogo 1 ou no jogo 2?</b></p>
<p><b>Jogador 2: Break no jogo 1 ou no jogo 2</b>: Aposta se o jogador 2 sofre o break no jogo 1 ou no jogo 2?</p>
<p><b>Resultado dos 2 primeiros sets</b>: Aposta se o resultado final após os dois primeiros sets será de 2-0, 1-1 ou 0-2.</p>





