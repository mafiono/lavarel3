<h2>Geral</h2>
<p>Se o jogo não for concluído, todas as apostas sobre a duração total do jogo serão anuladas.</p>
<p>Se o local do jogo for alterado, as apostas já colocadas serão válidas desde que a equipa da casa continue a ser designada como tal. Se a equipa da casa e a equipa visitante de um jogo apresentado forem trocadas, então as apostas baseadas na listagem original serão anuladas.</p>
<p>Jogos cancelados ou adiados são nulos a não ser que sejam remarcados e jogados na mesma data (tempo local) ou que seja indicado o contrário nas regras. São abertas exceções caso a hora de início anunciada no nosso website seja incorreta.</p>
