<p>Todos os jogos devem começar na data prevista (hora e local) para as apostas serem válidas.</p>
<p>Partidas adiadas ou abandonadas serão canceladas a menos que remarcados e jogados na mesma data (hora e local) ou indicação em contrário nas regras. A exceção é se nós anunciamos uma hora de início incorreta.</p>
<p>Se a equipa da casa e a equipa de fora para um jogo listado forem trocadas, então as apostas baseadas na listagem original serão anuladas.</p>
<p>Se um recinto de jogo é alterado, as apostas serão anuladas, salvo indicação contrária.</p>
<p>Em 2-way mercados de push regras aplicam-se, salvo indicação contrária. Stakes em apostas simples são devolvidos, e em múltiplos / Parlays a seleção é tratado como um non runner.</p>
<p>Para os Jogos Olímpicos, será aplicada seguintes regras:</p>
<ul>
    <li>A apresentação do pódio irá determinar a liquidação das apostas.</li>
    <li>Inibição de direitos e / ou recursos subsequentes não afetará as apostas.</li>
</ul>