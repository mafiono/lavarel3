<h2>Roleta Francesa Online</h2>

<ol>
    <li>A roleta francesa online é um jogo de fortuna ou de azar, em que os jogadores apostam contra a entidade exploradora num número, num conjunto de números e cor.</li>
    <li>A exploração e prática do jogo roleta francesa são realizadas em tempo real e num ambiente virtual que reproduza as características e utensílios utilizados numa mesa física e o emular das operações e seu funcionamento, designadamente, a roleta com o cilindro e prato, a representação de uma bola que se põe em movimento no interior do cilindro até se imobilizar num dos números do prato e o tabuleiro em formato retangular com a zona para marcação das apostas.</li>
    <li>Para efeitos das regras fixadas no presente Regulamento entende-se por:
        <ol type="a">
            <li>«Aposta», quantidade de fichas ou créditos, representativos de dinheiro expresso em euros, que o jogador coloca em jogo numa ou em várias chances de aposta;</li>
            <li>«Aposta máxima», valor máximo expresso em euros que o jogador pode apostar em cada uma das chances numa jogada;</li>
            <li>«Aposta mínima», valor mínimo expresso em euros que o jogador tem que apostar em cada uma das chances;</li>
            <li>«Jogada», operações que se iniciam com a realização das apostas pelos jogadores e se desenvolvem com o lançamento e imobilização da bola num número e terminam com a recolha das fichas perdedoras e o pagamento dos prémios aos vencedores;</li>
            <li>«Sessão de jogo», período de tempo ininterrupto que decorre entre o início e termo do acesso ao jogo da roleta francesa pelo jogador;</li>
        </ol>
    </li>
    <li>Na exploração e prática da roleta francesa podem ser utilizadas roletas individuais ou para múltiplos jogadores.</li>
    <li>Nas roletas para múltiplos jogadores cada jogador aposta individualmente contra a entidade exploradora e todos se vinculam ao resultado da jogada que é simultânea e comum a todos os participantes.</li>
    <li>A representação gráfica e emulação da roleta e dos utensílios de jogo deve conter, nomeadamente, as seguintes características:
        <ol type="a">
            <li>A roleta deve conter 37 números, uniformemente separados;</li>
            <li>Cada número é inscrito alternadamente sobre um fundo vermelho e preto, com exceção do n.º 0 cujo fundo é branco ou verde;</li>
            <li>A numeração é feita, no sentido dos ponteiros do relógio, com a seguinte sequência: 0 -32 -15 -19 -4 -21 -2 -25 -17 -34 -6 -27 -13 -36 -11 -30 -8 - 23 -10 -5 -24 -16 -33 -1 -20 -14 -31 -9 -22 -18 -29 -7 -28 -12 -35 -3 -26;</li>
            <li>Um tabuleiro marcado com as possibilidades de aposta, com um espaço reservado ao 0 e os restantes 36 números dispostos sequencialmente em três colunas de doze, depois dos retângulos dos números 34, 35 e 36 três retângulos para a marcação das apostas nas colunas, bem como espaços para marcações das apostas na dúzia, coluna, par, impar, menor, maior, preto e vermelho;</li>
            <li>Um mostrador onde são exibidos os últimos números vencedores e a respetiva cor.</li>
        </ol>
    </li>
    <li>O jogador pode apostar isolada ou conjuntamente em qualquer uma das seguintes apostas:
        <ol type="a">
            <li>Nas chances múltiplas, em:
                <ol type="i">
                    <li>Pleno — 1 número;</li>
                    <li>Cavalo — 2 números;</li>
                    <li>Rua — 3 números;</li>
                    <li>Quadro — 4 números;</li>
                    <li>Linha — 6 números;</li>
                    <li>Dúzia— 12 números alinhados de 1 a 12, ou 13 a 24, ou 25 a 36;</li>
                    <li>Coluna — 12 números, alinhados a partir do 34, do 35, ou do 36;</li>
                    <li>Cavalo de dúzia — 24 números;</li>
                    <li>Cavalo de coluna — 24 números;</li>
                </ol>
            </li>
            <li>Nas chances simples, em:
                <ol type="i">
                    <li>Par — os 18 números pares;</li>
                    <li>Ímpar — os 18 números ímpares;</li>
                    <li>Menor — os números 1 a 18;</li>
                    <li>Maior — os números 19 a 36;</li>
                    <li>Encarnado — os números encarnados;</li>
                    <li>Preto — os números pretos.</li>
                </ol>
            </li>
        </ol>
    </li>
    <li>Nos termos a definir nas regras específicas a entidade exploradora pode ainda explorar apostas “call bets” em conjuntos e séries de números de acordo com a sequência dos números no prato da roleta, nomeadamente:
        <ol type="a">
            <li>Na série 0 -2 -3 ou vizinhos do zero ou série (voisins du zero) — aposta de 17 números da roleta entre o 22 e 25 (22, 18, 29, 7, 28, 12, 35, 3, 26, 0, 32, 15, 19, 4, 21, 2, 25);</li>
            <li>Na série 5 -8 (tiers du cylindre) — aposta de 12 números da roleta entre o 27 e 33 (27, 13, 36, 11, 30, 8, 23, 10, 5, 24, 16, 33);</li>
            <li>Na série órfãos (Orphelins) — aposta de oito números que não estão incluídos nas apostas das séries 0 -2 -3 e 5 -8 (17, 34, 6 e 1, 20, 14, 31, 9);</li>
            <li>Num número e seus vizinhos:
                <ol type="i">
                    <li>Um número e dois números vizinhos;</li>
                    <li>Um número e quatro números vizinhos;</li>
                    <li>Um número e seis vizinhos;</li>
                    <li>Nos números finais — aposta nos números cuja terminação é igual.</li>
                </ol>
            </li>
        </ol>
    </li>
    <li>As apostas são marcadas pelo jogador nas respetivas zonas do tabuleiro reservado às apostas.</li>
    <li>Cada jogador pode apostar em qualquer uma das chances referidas na regra n.º 7 e, dentro de cada chance, em todas as combinações que sejam possíveis.</li>
    <li>O valor da aposta mínima de cada uma das chances é fixado pela entidade exploradora.</li>
    <li>O valor da aposta máxima em cada das chances é determinado a partir do valor da aposta mínima, de acordo com as seguintes proporções:
        <ol type="a">
            <li>Em cada uma das chances múltiplas:
                <ol type="i">
                    <li>Pleno: até 30 vezes o valor da aposta mínima;</li>
                    <li>Cavalo: até 60 vezes o valor da aposta mínima;</li>
                    <li>Rua: até 90 vezes o valor da aposta mínima;</li>
                    <li>Quadro: até 120 vezes o valor da aposta mínima;</li>
                    <li>Linha: até 180 vezes o valor da aposta mínima;</li>
                    <li>Dúzia: até 360 vezes o valor da aposta mínima;</li>
                    <li>Coluna: até 360 vezes o valor da aposta mínima;</li>
                    <li>Cavalo de dúzia: até 720 vezes o valor da aposta mínima;</li>
                    <li>Cavalo de coluna: até 720 vezes o valor da aposta mínima;</li>
                </ol>
            </li>
            <li>Em cada uma das chances simples: até 540 vezes o valor da aposta mínima.</li>
        </ol>
    </li>
    <li>A entidade exploradora pode não explorar as apostas em “cavalo de dúzia” e “cavalo de coluna”, bem como estabelecer nas regras específicas um limite máximo de aposta por jogador para o conjunto total de chances.</li>
    <li>As apostas nos números, conjunto de números e chances vencedoras continuam a pertencer ao jogador e dão lugar aos seguintes prémios:
        <ol type="a">
            <li>Chances múltiplas:
                <ol type="i">
                    <li>Pleno: 35 vezes o valor da aposta;</li>
                    <li>Cavalo: 17 vezes o valor da aposta;</li>
                    <li>Rua: 11: vezes o valor da aposta;</li>
                    <li>Quadro: 8 vezes o valor da aposta;</li>
                    <li>Linha: 5 vezes o valor da aposta;</li>
                    <li>Dúzia: 2 vezes o valor da aposta;</li>
                    <li>Coluna: 2 vezes o valor da aposta;</li>
                    <li>Cavalo de dúzia: metade do valor da aposta;</li>
                    <li>Cavalo de coluna: metade do valor da aposta;</li>
                </ol>
            </li>
            <li>Chances simples (par, ímpar, menor, maior, vermelho e preto): valor igual ao da aposta;</li>
        </ol>
    </li>
    <li>Nas combinações de apostas referidas na regra n.º 8 são vencedoras apenas as apostas que integrem o número vencedor da jogada, que são pagas nos termos das regras previstas no número anterior.</li>
    <li>Nas roletas para múltiplos jogadores, o período de tempo para a realização das apostas é definido pela entidade exploradora, corre em simultâneo para todos os jogadores e o momento em que não são aceites mais apostas deve estar claramente assinalado por um temporizador ou através de anúncios sonoros ou de texto, pelas expressões, respetivamente, «façam as vossas apostas» e «jogo feito nada mais» ou equivalentes.</li>
    <li>Nas roletas individuais o acionamento do lançamento da bola pode ser feito pelo próprio jogador após a realização da aposta.</li>
    <li>O número sorteado é assinalado no tabuleiro.</li>
    <li>Em seguida são recolhidas as fichas das apostas nas chances perdedoras e pagas as apostas vencedoras.</li>
    <li>O jogador ganha se as suas apostas coincidirem ou integrarem o número ou a cor em que a bola se imobilizou e perdem as suas apostas sempre que estas não coincidam ou integrem o número ou cor que saiu vencedor na jogada.</li>
    <li>A saída do 0 faz perder todas as apostas nas chances simples e em todas as chances múltiplas cujas apostas não integrem esses números, com exceção das apostas nos plenos, cavalos, ruas e quadros que integrem aqueles números.</li>
    <li>São consideradas nulas as jogadas em que, por avaria ou mau funcionamento, não é determinado um número sorteado válido.</li>
    <li>Nas jogadas nulas as apostas realizadas são devolvidas aos jogadores.</li>
    <li>Para efeitos do disposto na regra n.º 22 não se consideram nulas as jogadas em que as avarias, mau funcionamento ou interrupção das comunicações ocorrem na rede de comunicações ou equipamentos utilizados pelo jogador para participar no jogo que o impossibilitem de, após a realização da aposta, visualizar o resultado da jogada.</li>
    <li>Nos casos referidos na regra anterior a jogada e a aposta realizada são válidas e caso a aposta seja vencedora o prémio é pago ao jogador e se for perdedora é recolhida.</li>
    <li>A entidade exploradora deve disponibilizar ao jogador uma opção de visualização que permita o acesso a todos os elementos essenciais da última jogada em que tenha participado, designadamente o resultado e valores apostados.</li>
    <li>A entidade exploradora deve disponibilizar ao jogador antes, durante e após a sessão de jogo informação clara, objetiva e adequada sobre as regras do jogo, valores das apostas, ganhos e perdas, bem como os mecanismos de reclamação.</li>
    <li>No final de cada sessão de jogo a entidade exploradora deve disponibilizar ao jogador informação sobre os montantes apostados e respetivos lucros ou perdas.</li>
    <li>O início, duração e termo de cada sessão é definido pelo jogador.</li>
    <li>Sem prejuízo do disposto na regra anterior, a entidade exploradora pode convidar o jogador a terminar a sessão de jogo, sempre que:
        <ol type="a">
            <li>Numa roleta individual o jogador não realize qualquer aposta num período superior a 3 minutos.</li>
            <li>Numa roleta com múltiplos jogadores um jogador não realize apostas em mais de 5 jogadas consecutivas.</li>
        </ol>
    </li>
    <li>Nas situações previstas na regra anterior, caso o jogador não termine voluntariamente a sessão de jogo, a entidade exploradora deve adverti-lo que a sessão será terminada se o jogador não realizar qualquer aposta, respetivamente, no minuto seguinte ou na próxima jogada.</li>
    <li>A entidade exploradora pode disponibilizar no seu sítio na Internet aplicações de demonstração com o jogo da roleta francesa com as mesmas características das utilizadas na exploração com recurso a dinheiro.</li>
    <li>As aplicações de demonstração apenas podem atribuir o prolongamento gratuito do jogo em função da pontuação obtida, que não pode ser substituído ou convertido em dinheiro, vouchers para jogo, bens ou serviços de qualquer natureza ou espécie.</li>
</ol>

