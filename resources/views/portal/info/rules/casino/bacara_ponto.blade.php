<h2>Bacará Ponto e Banca</h2>

<h4>CAPÍTULO I (Regras do bacará ponto e banca)</h4>
<ol start="1">
    <li>O bacará ponto e banca/Macau é um jogo de fortuna ou azar, jogado com cartas, em que os jogadores apostam no jogo do ponto ou no da banca na expectativa de, na chance em que apostam, conseguirem uma combinação de cartas com a pontuação de 9 ou a que mais desta se aproxime, sem a exceder.</li>
    <li>Para efeitos das regras fixadas no presente Regulamento entende -se por:
        <ol type="a">
            <li>«Aposta máxima», valor máximo expresso em euros que o jogador pode apostar numa jogada;</li>
            <li>«Aposta mínima», valor mínimo expresso em euros que o jogador tem que apostar numa jogada;</li>
            <li>«Jogada», operações que se iniciam com a realização das apostas pelos jogadores, se desenvolvem com a distribuição das cartas de jogo pelo grupo do ponto e pelo grupo da banca, prosseguem com a contabilização das pontuações obtidas por cada um e termina com a recolha das fichas perdedoras e o pagamento dos prémios aos vencedores;</li>
            <li>«Sessão de jogo», período de tempo ininterrupto que decorre entre o início e termo do acesso ao jogo de ponto e banca pelo jogador;</li>
            <li>«Um par», duas cartas iguais do mesmo ou de naipe diferente.</li>
        </ol>
    </li>
    <li>A exploração e prática do jogo de ponto e banca online deve decorrer em tempo real num ambiente virtual que reproduza gráfica e visualmente as características, os equipamentos utilizados numa banca física e a emulação das operações e o seu funcionamento.</li>
    <li>Na exploração e prática do ponto e banca online podem utilizar- -se mesas individuais ou de múltiplos jogadores.</li>
    <li>Nas mesas de múltiplos jogadores, cada jogador aposta individualmente e todos se vinculam ao resultado da jogada que é simultânea e comum a todos os participantes.</li>
    <li>A representação gráfica e visual da mesa e dos utensílios de jogo deve conter, nomeadamente, as seguintes características:
        <ol type="a">
            <li>Mesas de um a nove lugares, contendo cada um deles, um espaço para a marcação das apostas no empate, na banca e no ponto;</li>
            <li>Utilização de seis ou oito baralhos de cartas;</li>
            <li>Cada baralho de cartas é composto por 52 cartas de quatro naipes, composto cada um deles por treze cartas ordenadas pelo valor do maior para o menor de ás, rei, dama, valete, 10, 9, 8, 7, 6, 5,4, 3, 2.</li>
        </ol>
    </li>
    <li>As cartas têm o seguinte valor:
        <ol type="a">
            <li>Ás — 1;</li>
            <li>Rei, dama, valete e 10 — 0;</li>
            <li>Todas as outras — o respetivo valor facial.</li>
        </ol>
    </li>
    <li>Quando a soma dos valores das cartas do ponto ou da banca ultrapassar o valor de 10 ou 20, considera -se como pontuação da chance apenas o valor do número que o exceda.</li>
    <li>Os jogadores podem apostar:
        <ol type="a">
            <li>No ponto;</li>
            <li>Na banca;</li>
            <li>No empate.</li>
        </ol>
    </li>
    <li>Adicionalmente às apostas referidas na regra anterior, a entidade exploradora pode permitir ainda apostas no par do ponto e no par da banca.</li>
    <li>Verifica -se par do ponto ou par da banca quando as duas cartas iniciais da respetiva chance formam um par, independentemente da sua cor ou do naipe.</li>
    <li>O rei, dama, valete e 10, apesar de terem o mesmo valor, apenas formam par, respetivamente, com rei, dama, valete e 10.</li>
    <li>Se um jogador apostar simultaneamente na chance do ponto e na chance da banca, a diferença de valor entre ambas as apostas não pode ser inferior ao valor da aposta mínima.</li>
    <li>O valor da aposta mínima está expresso em euros e é fixado pela entidade exploradora.</li>
    <li>Os valores máximos de aposta são os seguintes:
        <ol type="a">
            <li>No ponto ou na banca, até 70 vezes o valor da aposta mínima;</li>
            <li>No empate, até 15 vezes o valor da aposta mínima;</li>
            <li>No par do ponto ou no par da banca, até 8 vezes o valor da aposta mínima.</li>
        </ol>
    </li>
    <li>Findo o período de tempo para a realização das apostas inicia -se a distribuição das duas cartas iniciais para o jogo do grupo do ponto e para o jogo do grupo da banca.</li>
    <li>As cartas iniciais são distribuídas com as faces viradas para baixo, uma de cada vez e alternadamente, começando -se pelo grupo do ponto.</li>
    <li>Finda a distribuição das cartas iniciais são viradas primeiro as do grupo do ponto e depois as do grupo da banca.</li>
    <li>Se após a distribuição das cartas iniciais alguma das chances (ponto ou banca) perfizer a pontuação de 8 ou 9 não são distribuídas cartas adicionais para qualquer uma das chances.</li>
    <li>Quando com as cartas iniciais o grupo do ponto ou o grupo da banca não obtiver a pontuação de 8 ou 9, cada grupo apenas pode receber uma carta adicional, sendo a primeira distribuída para o grupo do ponto, de acordo com a seguinte tabela:</li>
