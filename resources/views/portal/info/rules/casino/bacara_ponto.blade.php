<ol start="21">
    <li>A carta adicional distribuída a cada uma das chances deve ficar exposta na mesa de forma a que claramente se diferencie das duas cartas iniciais.</li>
    <li>O jogador ganha as suas apostas se a chance em que apostar tiver a pontuação mais elevada e perde as suas apostas se a chance contrária a que apostar tiver a pontuação mais elevada.</li>
    <li>Se as pontuações do jogo do ponto e do jogo da banca forem coincidentes, são vencedoras as apostas no empate e as apostas realizadas em qualquer uma daquelas chances não perdem nem ganham.</li>
    <li>O valor das apostas vencedoras continua a pertencer ao jogador.</li>
    <li>As apostas vencedoras são pagas de acordo com as seguintes proporções:
        <ol type="a">
            <li>No ponto ou na banca, valor igual ao da aposta;</li>
            <li>No empate, oito vezes o valor da aposta;</li>
            <li>No par do ponto ou no par da banca, 11 vezes o valor da aposta.</li>
        </ol>
    </li>
    <li>A entidade explorada pode cobrar uma comissão sobre os prémios, que é deduzida ao pagamento, segundo uma das seguintes modalidades:
        <ol type="a">
            <li>Dedução de 5 % sobre o valor dos prémios de cada jogada ganho pelo grupo da banca;</li>
            <li>Dedução de 50 % sobre o valor dos prémios nas jogadas ganhas com a pontuação de 5 ou de 6 pelo grupo da banca.</li>
        </ol>
    </li>
    <li>Nas mesas com múltiplos jogadores, o período de tempo para a realização das apostas é definido pela entidade exploradora, corre em simultâneo para todos os jogadores e deve estar claramente assinalado por um temporizador ou através de anúncios de texto ou sonoros, pelas expressões, respetivamente, «façam as vossas apostas» e «jogo feito nada mais, cartas em distribuição» ou expressões equivalentes.</li>
    <li>Nas mesas individuais o início da distribuição das cartas pode ser feito pelo próprio jogador após a marcação da aposta.</li>
    <li>Quando, por motivo de avaria ou mau funcionamento, as jogadas sejam interrompidas sem que seja possível determinar a pontuação do grupo da banca ou do grupo do ponto a jogada é nula e o valor das apostas realizadas é devolvido aos jogadores.</li>
    <li>Para efeitos do disposto na regra anterior, não se consideram nulas as jogadas em que as avarias, mau funcionamento ou interrupção das comunicações ocorram na rede de comunicações ou equipamentos utilizados pelo jogador para participar no jogo e que o impossibilitem de, após a realização da aposta, visualizar o resultado da jogada, sendo que, no caso de sair vencedora a chance em que o jogador apostou é -lhe pago o prémio nos termos das regras do n.º 25 e, se sair perdedora, perde os valores apostados.</li>
    <li>A entidade exploradora deve disponibilizar ao jogador uma opção que permita a visualização de todos os elementos essenciais da última jogada, designadamente o resultado e valores apostados.</li>
    <li>A entidade exploradora deve disponibilizar ao jogador antes, durante e após a sessão de jogo informação clara, objetiva e adequada sobre as regras do jogo, valores mínimos e máximos das apostas, ganhos e perdas, bem como os mecanismos de reclamação.</li>
    <li>No final de cada sessão de jogo a entidade exploradora deve disponibilizar ao jogador informação sobre os montantes apostados e respetivos lucros ou perdas.</li>
    <li>O início, duração e termo de cada sessão é definido pelo jogador.</li>
    <li>Sem prejuízo do disposto na regra anterior, a entidade exploradora pode convidar o jogador a terminar a sessão de jogo sempre que:
        <ol type="a">
            <li>Numa mesa individual o jogador não faz qualquer aposta num período superior a 3 minutos;</li>
            <li>Numa mesa com múltiplos jogadores o jogador não realize apostas em mais de 5 jogadas consecutivas.</li>
        </ol>
    </li>
    <li>Nas situações previstas na regra anterior, caso o jogador não termine voluntariamente a sessão de jogo, a entidade exploradora deve adverti-lo que a sessão será terminada se o jogador não realizar qualquer aposta, respetivamente, no minuto seguinte ou na próxima jogada.</li>
    <li>A entidade exploradora pode disponibilizar no seu sítio na Internet aplicações de demonstração com o jogo de ponto e banca com as mesmas características das utilizadas na exploração com recurso a dinheiro.</li>
    <li>As aplicações de demonstração apenas podem atribuir o prolongamento gratuito do jogo em função da pontuação obtida, que não pode ser substituído ou convertido em dinheiro, vouchers para jogo, bens ou serviços de qualquer natureza ou espécie.</li>
</ol>

<h4>CAPÍTULO II (Regras do bacará ponto e banca Macau)</h4>

<ol start="39">
    <li>À exploração e prática do jogo de bacará ponto e banca Macau são aplicáveis as regras do bacará ponto e banca previstas no Capítulo anterior, com exceção da regra n.º 15.</li>
    <li>O valor das apostas individuais por jogador e por chance é ilimitado.</li>
    <li>A entidade exploradora pode fixar um valor máximo de prémios a pagar em cada jogada.</li>
    <li>O valor referido na regra anterior é aplicado sobre a diferença entre os valores das apostas no ponto e os na banca.</li>
    <li>Caso a entidade exploradora fixe um limite máximo ao pagamento dos prémios, os valores máximos de aposta são limitados nos seguintes termos:
        <ol type="a">
            <li>A aposta ou conjunto de apostas numa das chances do ponto ou da banca não pode exceder os da outra chance em valor superior ao montante referido na regra n.º 41;</li>
            <li>O valor total das apostas no empate não pode exceder 10 % do valor referido na regra n.º 42;</li>
            <li>O valor máximo de todas as apostas no par do ponto ou no par da banca não pode exceder 8 % do valor referido na regra n.º 42. 16 de novembro de 2015. — A Vice -Presidente do Conselho Diretivo, Maria Teresa Rodrigues Monteiro.</li>
        </ol>
    </li>
</ol>

