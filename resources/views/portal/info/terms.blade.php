    <h2>Contingências de Apostas Relacionadas</h2>

    Apostas múltiplas que combinem selecções diferentes dentro do mesmo evento não são

    aceites nos casos em que o resultado de uma afecte ou seja afectado pelo resultado de

    outra.

    • Por exemplo, num jogo de futebol entre o Arsenal e o Everton:

    Se o Arsenal tem odds de 2.00 para vencer em casa contra o Everton e as odds do

    resultado correcto para o Arsenal vencer por 1-0 são de 9.00, por ter apostado duplo no

    Arsenal para vencer por 1-0, um jogador obteria odds de 18.00, numa aposta que tem

    como odd 9.00 para uma vitória condicional de 1-0 do Arsenal. Tal não é permitido, pois a

    partir do momento que o Arsenal vence por 1-0 significa que automaticamente vence o

    jogo.

    Caso esta aposta seja efectuada por erro, esta será estabelecida como duas apostas

    simples igualmente divididas.

    Uma excepção a esta regra resulta do facto de odds especiais serem oferecidas para

    uma eventualidade combinada, ex: Primeiro Jogador a Marcar/Duplo Resultado Correcto,

    conhecido como Scorecast.

    No caso de partes relacionadas da aposta serem resolvidas em momentos diferentes

    mas dentro do mesmo torneio, a aposta será decidida com base no segundo preço da

    selecção subsequente ser determinado em cada etapa individual.

    • Por exemplo, uma selecção dupla:

    Federer para ganhar as semi-finais de Wimbledon, e Federer para ser o vencedor final do

    torneio de Wimbledon

    Devido às odds da segunda selecção dependerem do resultados da primeira, as odds de

    Roger Federer ganhar o torneio no momento que a aposta for lançada não poderão ser

    usadas para calcular a dupla e as odds correctas serão atribuídas após a primeira

    selecção ter sido completada.

    Apostas múltiplas que combinam as mesmas selecções dentro de eventos diferentes

    também não serão aceites em casos nos quais o resultado de uma afecte o resultado de

    outra.

    Por exemplo, uma dupla selecção

    King’s Best para vencer o 2000 Guineas

    King’s Best para vencer o Derby

    Devido ao facto de que as odds para o King’s Best ganhar o Derby dependem do facto de

    ele ganhar o 2000 Guineas, as odds para ele ganhar o Derby no momento que a aposta

    foi lançada não poderão ser usadas para calcular a dupla. Neste caso, será oferecido um

    preço especial para ganhar ambas as corridas.

    No caso de todas as múltiplas incluirem selecções com resultados relacionados,

    devido à aposta ter sido feita por erro, a Bet Portugal reserva o direito de anular parte ou

    a totalidade da aposta.

