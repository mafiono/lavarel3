    <h2>Dead-Heats</h2>

    Quando ocorrer um Dead-Heat entre duas selecções, o dinheiro da aposta é

    proporcionado de acordo com o número de selecções incluídas no dead-heat e pago com

    odds completas.

    <h2>Apostas Condicionais</h2>

    As Apostas Condicionais são aceites em todos os desportos apenas para a opção vitória.

    Poderá optar por prosseguir de uma selecção para outra, com o mesmo valor de aposta,

    ou com uma parte especifica dos seus potenciais ganhos, progressivamente pela sua

    lista de apostas condicionais. O desfecho do resultado das Apostas condicionais será

    obtido através da ordem em que as apostas aparecem no seu boletim de apostas, até

    que todo o dinheiro que tenha posto de parte seja usado. Se o dinheiro apostado (ou

    subsequentemente encaminhado) para a próxima selecção for insuficiente para seguir as

    suas instrucções, o valor da aposta na sua selecção será determinado tendo como base

    os fundos disponíveis.

    Por favor, tenha em conta, de que o valor da sua aposta condicional não deve exceder o

    valor da sua aposta anterior.

    <h2>Apostas Condicionais Invertidas</h2>

    Poderá seleccionar qualquer número de selecções (máximo de 14) em 2 apostas

    condicionais invertidas. As regras de Apostas Condicionais Invertidas aplicam-se, mas a

    unidade de aposta permanecerá a mesma durante toda a aposta. Para a parte da ordem

    invertida da sua aposta, o montante da aposta que provir dos retornos da última selecção

    no seu boletim de apostas passará imediatamente para a penúltima selecção do seu

    boletim etc (ex: os desfechos das apostas são definidos na ordem inversa à que surge no

    seu boletim de apostas).

    Para todas as apostas condicionais e apostas condicionais invertidas, os clientes quando

    colocarem a aposta têm a escolha de seleccionar a opção Acção Única ou Acção Dupla,

    poderão também optar por seleccionar a opção de parar a aposta se esta for anulada ou

    quando o particpante em que se apostou não participa na prova.

